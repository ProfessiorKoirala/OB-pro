// Reference date: 14th April 1943 AD which is 1st Baishakh 2000 BS
const refEngYear = 1943;
const refEngMonth = 4;
const refEngDay = 14;
const refNepYear = 2000;
const refNepMonth = 1;
const refNepDay = 1;

export const toNepaliDate = (gregorianDate: Date) => {
    const year = gregorianDate.getFullYear();
    const month = gregorianDate.getMonth() + 1;
    const day = gregorianDate.getDate();

    // Check if the date is within the convertible range
    if (year < refEngYear || year > refEngYear + 99) {
        return null;
    }

    const startDate = new Date(refEngYear, refEngMonth - 1, refEngDay);
    startDate.setHours(12, 0, 0, 0); // Set to noon to avoid DST issues
    const endDate = new Date(year, month - 1, day);
    endDate.setHours(12, 0, 0, 0);
    let totalEngDays = Math.round((endDate.getTime() - startDate.getTime()) / (1000 * 60 * 60 * 24));

    let nepYear, nepMonth, nepDay;

    for (let y = 0; y < nepaliMonthDays.length; y++) {
        for (let m = 0; m < 12; m++) {
            const daysInMonth = nepaliMonthDays[y][m];
            if (totalEngDays < daysInMonth) {
                nepDay = totalEngDays + 1;
                nepYear = refNepYear + y;
                nepMonth = m + 1;
                return {
                    year: nepYear,
                    month: nepMonth,
                    day: nepDay,
                    monthName: nepaliMonths[nepMonth - 1]
                };
            }
            totalEngDays -= daysInMonth;
        }
    }

    return null;
};

export const getNepaliDay = (gregorianDate: Date): number | null => {
    const nepaliDate = toNepaliDate(gregorianDate);
    return nepaliDate ? nepaliDate.day : null;
};
